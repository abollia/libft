#include "libft.h"

void	ft_ffa(char ***tab)
{
	size_t	i;

	if (tab && *tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			free((*tab)[i]);
			(*tab)[i] = NULL;
			i++;
		}
		free(*tab);
		*tab = NULL;
	}
}
